-- Get trending posts (most views in last 7 days)
CREATE OR REPLACE FUNCTION get_trending_posts(limit_count integer DEFAULT 10)
RETURNS TABLE (
  post_id bigint,
  title text,
  view_count bigint,
  like_count bigint,
  trend_score numeric
) AS $$
BEGIN
  RETURN QUERY
  SELECT 
    ps.post_id,
    ps.title,
    ps.view_count,
    ps.like_count,
    (ps.views_last_7_days * 1.0 + ps.likes_last_7_days * 2.0) as trend_score
  FROM post_statistics ps
  ORDER BY trend_score DESC
  LIMIT limit_count;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Get user post statistics
CREATE OR REPLACE FUNCTION get_user_stats(p_user_id text)
RETURNS TABLE (
  total_posts bigint,
  total_views bigint,
  total_likes bigint,
  total_comments bigint,
  avg_views_per_post numeric,
  avg_likes_per_post numeric
) AS $$
BEGIN
  RETURN QUERY
  SELECT 
    COUNT(*)::bigint as total_posts,
    COALESCE(SUM(ps.view_count), 0)::bigint as total_views,
    COALESCE(SUM(ps.like_count), 0)::bigint as total_likes,
    COALESCE(SUM(ps.comment_count), 0)::bigint as total_comments,
    COALESCE(AVG(ps.view_count), 0) as avg_views_per_post,
    COALESCE(AVG(ps.like_count), 0) as avg_likes_per_post
  FROM post_statistics ps
  WHERE ps.user_id = p_user_id;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Record a post view (with deduplication)
CREATE OR REPLACE FUNCTION record_post_view(
  p_post_id bigint,
  p_user_id text DEFAULT NULL,
  p_ip_address inet DEFAULT NULL,
  p_user_agent text DEFAULT NULL
)
RETURNS void AS $$
DECLARE
  existing_view_id bigint;
BEGIN
  -- Check if user viewed this post in last hour (deduplication)
  IF p_user_id IS NOT NULL THEN
    SELECT id INTO existing_view_id
    FROM post_views
    WHERE post_id = p_post_id
      AND user_id = p_user_id
      AND viewed_at > now() - interval '1 hour'
    LIMIT 1;
    
    IF existing_view_id IS NOT NULL THEN
      -- Already counted this view recently
      RETURN;
    END IF;
  END IF;
  
  -- Record the view
  INSERT INTO post_views (post_id, user_id, ip_address, user_agent)
  VALUES (p_post_id, p_user_id, p_ip_address, p_user_agent);
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

